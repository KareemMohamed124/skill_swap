 class Session {
  final String id;
  final String image;
  final String name;
  final String role;
  final String type;
  final DateTime dateTime;
  final String price;
  final String status;
  final String timeAgo;

  Session({
    required this.id,
    required this.image,
    required this.name,
    required this.role,
    required this.type,
    required this.dateTime,
    required this.price,
    required this.status,
    required this.timeAgo
  });
 }